#!/usr/bin/env node
/**
 * Regenerates shared/src/taxExemptions.ts from the wiki's
 * "Items exempt from Grand Exchange tax" category, resolving page titles to
 * item ids via the real-time prices /mapping endpoint.
 *
 * Usage: node scripts/generate-tax-exemptions.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const UA = process.env.WIKI_USER_AGENT ?? 'ge-flip-finder/1.0 (contact: unset)';
const CATEGORY_URL =
  'https://oldschool.runescape.wiki/api.php?action=query&list=categorymembers' +
  '&cmtitle=Category:Items_exempt_from_Grand_Exchange_tax&cmlimit=500&format=json';
const MAPPING_URL = 'https://prices.runescape.wiki/api/v1/osrs/mapping';

const [category, mapping] = await Promise.all([
  fetch(CATEGORY_URL, { headers: { 'User-Agent': UA } }).then((r) => r.json()),
  fetch(MAPPING_URL, { headers: { 'User-Agent': UA } }).then((r) => r.json()),
]);

const titles = category.query.categorymembers.filter((m) => m.ns === 0).map((m) => m.title);
const byName = new Map(mapping.map((item) => [item.name, item]));

/** Page titles are wiki-disambiguated; GE names differ for tablets, charged
 *  jewellery and dosed potions. Try exact, then known transforms. */
function resolve(title) {
  if (byName.has(title)) return [byName.get(title)];
  const deTablet = title.replace(/ \(tablet\)$/, '');
  if (byName.has(deTablet)) return [byName.get(deTablet)];
  // Charge/dose variants: "Ring of dueling" -> "Ring of dueling(8)", "Energy potion" -> (1)-(4)
  const variants = mapping.filter((i) => i.name.startsWith(`${title}(`));
  return variants.length > 0 ? variants : [];
}

const resolved = [];
const unresolved = [];
for (const title of titles) {
  const items = resolve(title);
  if (items.length === 0) unresolved.push(title);
  for (const item of items) resolved.push({ id: item.id, name: item.name, page: title });
}
resolved.sort((a, b) => a.id - b.id);

if (unresolved.length > 0) {
  console.warn('WARNING: could not resolve to item ids:', unresolved);
}

const header = `// AUTO-GENERATED by scripts/generate-tax-exemptions.mjs — do not edit by hand.
// Source: https://oldschool.runescape.wiki/w/Category:Items_exempt_from_Grand_Exchange_tax
// ${titles.length} wiki pages -> ${resolved.length} tradeable item variants.

/** Items exempt from the 2% Grand Exchange tax. */
export const GE_TAX_EXEMPT_ITEMS: ReadonlyArray<{ id: number; name: string }> = [
`;
const rows = resolved.map((r) => `  { id: ${r.id}, name: ${JSON.stringify(r.name)} },`).join('\n');
const footer = `
];

export const GE_TAX_EXEMPT_IDS: ReadonlySet<number> = new Set(
  GE_TAX_EXEMPT_ITEMS.map((i) => i.id),
);
`;

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, '..', 'shared', 'src', 'taxExemptions.ts');
fs.writeFileSync(outPath, header + rows + footer);
console.log(`Wrote ${resolved.length} exempt items to ${outPath}`);
