#!/usr/bin/env node
/**
 * Regenerates client/src/data/itemSets.ts from the wiki: finds every tradeable
 * "... set" item in /mapping, fetches each set page's wikitext, and extracts
 * the ==Components== {{CostLine|...}} entries. Sets whose components don't all
 * resolve to tradeable items are skipped (logged).
 *
 * Usage: node scripts/generate-item-sets.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const UA = process.env.WIKI_USER_AGENT ?? 'ge-flip-finder/1.0 (contact: unset)';
const MAPPING_URL = 'https://prices.runescape.wiki/api/v1/osrs/mapping';

const mapping = await fetch(MAPPING_URL, { headers: { 'User-Agent': UA } }).then((r) => r.json());
const byName = new Map(mapping.map((i) => [i.name, i]));

const setItems = mapping
  .filter((i) => / set( \((lg|sk)\))?$/.test(i.name) || / set$/.test(i.name))
  .sort((a, b) => a.name.localeCompare(b.name));
console.log(`${setItems.length} set items found in mapping`);

async function fetchComponents(setName) {
  const url = `https://oldschool.runescape.wiki/api.php?action=parse&page=${encodeURIComponent(
    setName.replaceAll(' ', '_'),
  )}&prop=wikitext&format=json&redirects=1`;
  const body = await fetch(url, { headers: { 'User-Agent': UA } }).then((r) => r.json());
  const wikitext = body?.parse?.wikitext?.['*'];
  if (!wikitext) return null;
  const section = wikitext.split(/==\s*Components\s*==/i)[1];
  if (!section) return null;
  const names = [...section.matchAll(/\{\{CostLine\|([^|}]+)/g)].map((m) => m[1].trim());
  return names.length > 0 ? names : null;
}

const sets = [];
const skipped = [];
for (const item of setItems) {
  const components = await fetchComponents(item.name);
  await new Promise((r) => setTimeout(r, 250)); // be polite to the wiki
  if (!components) {
    skipped.push(`${item.name} (no components section)`);
    continue;
  }
  const pieces = components.map((n) => byName.get(n));
  if (pieces.some((p) => p === undefined)) {
    skipped.push(`${item.name} (unresolved: ${components.filter((n) => !byName.has(n)).join(', ')})`);
    continue;
  }
  sets.push({
    setId: item.id,
    setName: item.name,
    pieces: pieces.map((p) => ({ id: p.id, name: p.name })),
  });
  process.stdout.write('.');
}
console.log(`\n${sets.length} sets resolved, ${skipped.length} skipped`);
skipped.forEach((s) => console.log('  skip:', s));

const header = `// AUTO-GENERATED by scripts/generate-item-sets.mjs — do not edit by hand.
// Grand Exchange item sets and their components (exchange via a GE clerk's "Sets" option).
// ${sets.length} sets resolved from the wiki on ${new Date().toISOString().slice(0, 10)}.

export interface ItemSetDef {
  setId: number;
  setName: string;
  pieces: { id: number; name: string }[];
}

export const ITEM_SETS: ItemSetDef[] = `;

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outPath = path.join(__dirname, '..', 'client', 'src', 'data', 'itemSets.ts');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, header + JSON.stringify(sets, null, 2) + ';\n');
console.log(`wrote ${outPath}`);
